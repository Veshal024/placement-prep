# Program 1: List basics
numbers = [10, 20, 30, 40, 50]
print(numbers)
# Program 2: Access list elements
print(numbers[0])   # first element
print(numbers[2])   # third element
print(numbers[-1])  # last element
# Program 3: Loop through list
for num in numbers:
    print(num)
# Program 4: Add and remove
numbers.append(60)
print(numbers)

numbers.remove(20)
print(numbers)
